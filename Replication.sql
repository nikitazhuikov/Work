SELECT 
    -- 1. Год и период
    N'Сентябрь 2025' AS [Год и период],
    
    -- 2. Субъект РФ (из _Тиражирование_практики)
    рег.[Name] AS [Субъект РФ],
    
    -- 3. Код лучшей практики
    спр.[Код_лучшей_практики] AS [Код лучшей практики],
    
    -- 4. Лучшая практика актуальна в субъекте РФ-авторе
    CASE спр.[Лучшая_практика_актуальна_в_субъекте_РФ_авторе]
        WHEN 5327 THEN N'Да'
        WHEN 5328 THEN N'Нет'
        ELSE NULL
    END AS [Лучшая практика актуальна в субъекте РФ-авторе],
    
    -- 5. Вид МО
    вид_мо.[Name] AS [Вид МО в которых могут быть использованы решения лучшей практики],
    
    -- 6. Процесс (hierarchical parent подпроцесса)
    процесс_parent.[Name] AS [Процесс, улучшаемый в результате использования решений лучшей практики в МО],
    
    -- 7. Подпроцесс
    процесс.[Name] AS [Подпроцесс, улучшаемый в результате использования решений лучшей практики в МО],
    
    -- 8-10. Сведения о тиражировании
    пр.[Дата_начала_тиражирования_лучшей_практики_в_МО_субъекта_РФ] AS [Дата начала тиражирования лучшей практики в МО субъекта РФ],
    пр.[Плановая_дата_завершения_тиражирования_лучшей_практики_в_МО_субъекта_РФ] AS [Плановая дата завершения тиражирования лучшей практики в МО субъекта РФ],
    пр.[Фактическая_дата_завершения_тиражирования_лучшей_практики_в_субъекте_РФ] AS [Фактическая дата завершения тиражирования лучшей практики в субъекте РФ],
    
    -- 11-12. Количество МО
    пр_представление.[Количество_МО_завершивших_тиражирование_с_достижением_значений_показателей_лучшей_практики] AS [Количество МО завершивших тиражирование с достижением значений показателей лучшей практики],
    пр_представление.[Количество_МО_в_которых_запланировано_тиражирование_лучшей_практики] AS [Количество МО в которых запланировано тиражирование лучшей практики],
    
    -- 13-18. Региональный стандарт
    орган_рег_стандарт.[Name] AS [Орган исполнительной власти субъекта Российской Федерации, утвердивший документ (регстандарт)],
    пр.[Дата_утверждения_документа_Региональный_стандарт] AS [Дата утверждения документа (регстандарт)],
    пр.[Номер_документа_Региональный_стандарт] AS [Номер документа (регстандарт)],
    пр.[Наименование_документа_на_основании_которого_ЛП_утверждена_в_качестве_регионального_стандарта] AS [Наименование документа на основании которого ЛП утверждена в качестве регионального стандарта],
    пр.[Документ_на_основании_которого_лучшая_практика_утверждена_в_качестве_регионального_стандарта] AS [Документ на основании которого лучшая практика утверждена в качестве регионального стандарта],
    вид_док_рег_стандарт.[Name] AS [Вид документа (регстандарт)],
    
    -- 19-20. Дата пересмотра и актуальность
    пр.[Дата_последнего_пересмотра_лучшей_практики] AS [Дата последнего пересмотра лучшей практики],
    CASE пр.[Лучшая_практика_актуальна]
        WHEN 5327 THEN N'Да'
        WHEN 5328 THEN N'Нет'
        ELSE NULL
    END AS [Лучшая практика актуальна],
    
    -- 21. Регион-автор (из _Тиражирование_практики_справочник)
    рег_автор.[Name] AS [Регион-автор],
    
    -- 22-23. Другие поля процессов
    спр.[Подпроцесс_улучшаемый_в_результате_использования_решений_лучшей_практики_в_МО_другое] AS [Подпроцесс, улучшаемый в результате использования решений лучшей практики в МО (другое)],
    спр.[Процесс_улучшаемый_в_результате_использования_решений_лучшей_практики_в_МО_другое] AS [Процесс, улучшаемый в результате использования решений лучшей практики в МО (другое)],
    
    -- 24-27. Дополнительная информация из справочника
    население.[Name] AS [Обслуживаемое население МО, в которых могут быть использованы решения лучшей практики],
    спр.[Дата_утверждения_лучшей_практики] AS [Дата утверждения лучшей практики],
    спр.[Дата_последнего_пересмотра_лучшей_практики_в_субъекте_РФ_авторе] AS [Дата последнего пересмотра лучшей практики в субъекте РФ - авторе],
    спр.[Комплект_документов_содержащих_сведения_о_решениях_лучшей_практики] AS [Комплект документов содержащих сведения о решениях лучшей практики],
    
    -- 28-32. Показатели
    показ_наим.[Name] AS [Наименование показателя, достигаемого в результате использования решений лучшей практики],
    показ_ед_изм.[Name] AS [Единица измерения],
    показ.[Наименование_показателя_достигаемого_в_результате_использования_решений_лучшей_практики_другое] AS [Наименование показателя, достигаемого в результате использования решений лучшей практики (другое)],
    показ.[Единица_измерения_другое] AS [Единица измерения (другое)],
    показ.[Значение] AS [Значение],
    
    -- 33-38. Документ тиражирование
    вид_док_тираж.[Name] AS [Вид документа (тиражирование)],
    орган_тираж.[Name] AS [Орган исполнительной власти субъекта Российской Федерации, утвердивший документ (тиражирование)],
    пр.[Дата_утверждения_документа_тиражирование] AS [Дата утверждения документа (тиражирование)],
    пр.[Номер_документа_тиражирование] AS [Номер документа (тиражирование)],
    пр.[Наименование_документа_на_основании_которого_выполняется_тиражирование_лучшей_практики_в_МО] AS [Наименование документа, на основании которого выполняется тиражирование лучшей практики в МО],
    пр.[Документ_на_основании_которого_выполняется_тиражирование_лучшей_практики_в_МО] AS [Документ, на основании которого выполняется тиражирование лучшей практики в МО],
    
    -- 39. Лучшая практика утверждена в качестве регионального стандарта
    CASE пр.[Лучшая_практика_утверждена_в_качестве_регионального_стандарта]
        WHEN 5327 THEN N'Да'
        WHEN 5328 THEN N'Нет'
        ELSE NULL
    END AS [Лучшая практика утверждена в качестве регионального стандарта]

FROM [ASMMS].[dbo].[_Тиражирование_показатели] показ

-- Связь с справочником практик через main_object_id
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_практики_справочник] спр
    ON показ.[main_object_id] = спр.[id]

-- Наименования показателей ПСМП
LEFT JOIN [ASMMS].[dbo].[_ПСМП_наименования_показателей] показ_наим
    ON показ.[ПСМП_наименования_показателей] = показ_наим.[id]

-- Единицы измерения ПСМП
LEFT JOIN [ASMMS].[dbo].[_ПСМП_единицы_измерения] показ_ед_изм
    ON показ.[ПСМП_единица_измерения] = показ_ед_изм.[id]

-- Связь с таблицей _Тиражирование_практики
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_практики] пр
    ON спр.[id] = пр.[Лучшая_практика]

-- Регион из _Тиражирование_практики
LEFT JOIN [ASMMS].[dbo].[_Регион] рег
    ON пр.[Регион] = рег.[Id]

-- Регион-автор из _Тиражирование_практики_справочник
LEFT JOIN [ASMMS].[dbo].[_Регион] рег_автор
    ON спр.[Регион_автор] = рег_автор.[Id]

-- Вид МО
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_Вид_МО] вид_мо
    ON спр.[Вид_МО_в_которых_могут_быть_использованы_решения_лучшей_практики] = вид_мо.[Id]

-- Обслуживаемое население
LEFT JOIN [ASMMS].[dbo].[_Обслуживаемое_население] население
    ON спр.[Обслуживаемое_население_МО_в_которых_могут_быть_использованы_решения_лучшей_практики] = население.[Id]

-- Процесс (подпроцесс)
LEFT JOIN [ASMMS].[dbo].[_Первичка_процессы] процесс
    ON спр.[Процесс] = процесс.[Id]

-- Процесс (родительский - hierarchical parent подпроцесса)
LEFT JOIN [ASMMS].[dbo].[_Первичка_процессы] процесс_parent
    ON процесс.[HierarchicalParent] = процесс_parent.[Id]

-- Вид документа (тиражирование)
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_вид_документа] вид_док_тираж
    ON пр.[Вид_документа] = вид_док_тираж.[Id]

-- Орган исполнительной власти (тиражирование)
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_орган_исполнительной_власти] орган_тираж
    ON пр.[Орган_исполнительной_власти_субъекта_Российской_Федерации_утвердивший_документ] = орган_тираж.[Id]

-- Вид документа (региональный стандарт)
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_вид_документа] вид_док_рег_стандарт
    ON пр.[Вид_документа_Региональный_стандарт] = вид_док_рег_стандарт.[Id]

-- Орган исполнительной власти (региональный стандарт)
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_орган_исполнительной_власти] орган_рег_стандарт
    ON пр.[Орган_исполнительной_власти_субъекта_Российской_Федерации_утвердивший_документ_Региональный_стандарт] = орган_рег_стандарт.[Id]

-- Представление тиражирования
LEFT JOIN [ASMMS].[dbo].[_Тиражирование_представление] пр_представление
    ON пр.[Регион] = пр_представление.[Регион]
    AND спр.[Код_лучшей_практики] = пр_представление.[Код_лучшей_практики]

ORDER BY 
    рег.[Name],
    спр.[Код_лучшей_практики],
    показ_наим.[Name];